    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date deadline = dateFormat.parse(txtDeadline.getText());
            Task updatedTask = new Task(taskId, txtJudul.getText(), txtDeskripsi.getText(), deadline, null);
            taskDAO.updateTask(updatedTask);
            JOptionPane.showMessageDialog(this, "Task updated successfully.", "Update Success", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error updating task: " + ex.getMessage(), "Update Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

