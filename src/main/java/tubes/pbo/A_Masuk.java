    private void btnMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukActionPerformed
        String username = txtUsername.getText();
        char[] password = txtPassword.getPassword();
        UserDAO userDao = new UserDAO();

        try {
            User user = userDao.getUserByUsername(username);
            if (user != null && new String(password).equals(user.getPassword())) {
                if (user.getType().equals("Ketua")) {
                    C_Menu_KETUA menuKetua = new C_Menu_KETUA(user.getId());
                    menuKetua.setVisible(true);
                    this.setVisible(false);
                } else if (user.getType().equals("Anggota")) {
                    I_Menu_ANGGOTA menuAnggota = new I_Menu_ANGGOTA(user.getId());
                    menuAnggota.setVisible(true);
                    this.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password", "Login Failed", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to connect to database", "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnMasukActionPerformed

